#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	index;
	char	*p;

	p = (char *)s;
	index = 0;
	while ((p[index] != '\0') && index < n)
	{
		p[index] = 0;
		index++;
	}
}
