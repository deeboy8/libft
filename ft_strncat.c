#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	len;
	int		i;
	int		j;

	i = 0;
	j = 0;
	len = 0;
	while (s1[i] != '\0' && len <= n)
	{
		++i;
		++len;
	}
	while (s2[j] != '\0' && len <= n)
	{
		s1[i] = s2[j];
		++i;
		++j;
		++len;
	}
	s1[i] = '\0';
	return (s1);
}
